    <script>
        "use strict"
        $("#form-products").submit(function(e) {
            e.preventDefault();
            const form = $(this);
            const actionUrl = form.attr("action");
            $.ajax({
                type: "POST",
                url: actionUrl,
                data: new FormData(this),
                contentType: false,
                cache: false,
                processData: false,
                success: function(response) {
                    if (response.errors) {
                        if (response.errors.category) {
                            $('#err_category').text(response.errors.category)
                        }
                        if (response.errors.name) {
                            $('#err_name').text(response.errors.name)
                        }
                        if (response.errors.quantity) {
                            $('#err_quantity').text(response.errors.quantity)
                        }
                        if (response.errors.db) {
                            Swal.fire({
                                title: 'Error!',
                                text: response.errors.db,
                                icon: 'error',
                                confirmButtonText: 'Tutup'
                            })
                        }
                    } else if (response.success) {
                        Swal.fire({
                            title: 'Success!',
                            text: response.success,
                            icon: 'success',
                            confirmButtonText: 'Tutup'
                        })
                        location.reload()
                    }
                },
            });
        });

        $("#productModal").on("show.bs.modal", function(e) {
            resetForm();
            select_categories()
            $("#productModal").modal("show");
        });

        $('#myModal').on('hidden.bs.modal', function(event) {
            resetForm();
        })

        function resetForm(is_success = false) {
            $('#err_category').text('')
            $('#err_name').text('')
            $('#err_quantity').text('')
            $('#name').val('')
            $('#id').val('')
            $('#quantity').val('')
        }
        const delete_product = (id) => {
            Swal.fire({
                title: "Do you want to delete the product?",
                showCancelButton: true,
                icon: 'question',
                confirmButtonText: "Delete",
            }).then((result) => {
                /* Read more about isConfirmed, isDenied below */
                if (result.isConfirmed) {
                    $.ajax({
                        url: "/products/" + id,
                        type: "DELETE",
                        headers: {
                            "X-CSRF-TOKEN": "{{ csrf_token() }}"
                        },
                        data: {
                            id: id,
                        },
                        success: function(response) {
                            if (response.errors) {
                                if (response.errors) {
                                    Swal.fire({
                                        title: "Error!",
                                        text: response.errors.data,
                                        icon: "error",
                                        confirmButtonText: "Tutup",
                                    });
                                }
                            } else if (response.success) {
                                Swal.fire({
                                    title: "Success!",
                                    text: response.success,
                                    icon: "success",
                                    confirmButtonText: "Tutup",
                                });
                                location.reload()
                            }
                        },
                    });
                }
            });
        }
        const edit_product = (id) => {
            $.ajax({
                url: "/products/" + id + "/edit",
                type: "GET",
                headers: {
                    "X-CSRF-TOKEN": "{{ csrf_token() }}"
                },
                success: function(response) {
                    console.log(response);
                    if (response.errors) {
                        Swal.fire({
                            title: "Error!",
                            text: response.errors.data,
                            icon: "error",
                            confirmButtonText: "Tutup",
                        });
                    }
                    if (response.result) {
                        resetForm();
                        set_data_edit(response)
                    }
                },
            });

        }
        const set_data_edit = (response) => {
            $('#id').val(response.result.id);
            $("#name").val(response.result.name);
            $("#quantity").val(response.result.quantity);
            select_categories(response.result.category_id);
        };
        const select_categories = (category_id = null) => {
            $.ajax({
                url: "{{ route('categories.data') }}",
                type: 'POST',
                headers: {
                    'X-CSRF-TOKEN': "{{ csrf_token() }}"
                },
                data: {
                    category_id: category_id
                },
                success: function(response) {
                    $('#category_id').html(response);
                }
            })
        }
    </script>
